def birthdayCakeCandles(candles):
    # Write your code here
    ctr = 0
    max_val = 0
    
    for c in candles:
        if c > max_val:
            max_val = c
            ctr = 1
        elif c == max_val:
            ctr += 1
        else:
            continue
    
    return ctr

